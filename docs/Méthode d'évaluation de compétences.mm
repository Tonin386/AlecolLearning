    <p>
      <b><font size="4">Th&#233;orie de R&#233;ponse aux Items (Item Response Theory ou IRT) </font></b>
    </p>
    <p>
      
    </p>
    <p>
      <b>Principe : </b>Th&#233;orie bas&#233;e sur le principe que la probabilit&#233; de r&#233;pondre correctement &#224; une question d&#233;pend de la comp&#233;tence de la personne mais aussi des caract&#233;ristiques de la question (difficult&#233;, pouvoir de discrimination et chance d'y r&#233;pondre correctement au hasard).
    </p>
    <p>
      Cette th&#233;orie est sup&#233;rieure &#224; la CTT, car elle permet une analyse plus fine, item par item, en tenant compte des diff&#233;rences entre les items dans l'&#233;valuation de la comp&#233;tence des r&#233;pondants. De plus, elle a des avantages th&#233;oriques comme l'invariance des &#233;chantillons d'items ou des r&#233;pondants qui procurent un avantage consid&#233;rable.
    </p>
    <p>
      
    </p>
    <p>
      <b>Hypoth&#232;ses :</b>
    </p>
    <p>
      Cette th&#233;orie est valable si toutes les questions reposent (comme la CTT) sur une seule comp&#233;tence (unidimensionnalit&#233;), et qu'il y a ind&#233;pendance locale entre les questions (les questions sont ind&#233;pendantes entre elles, aucun lien entre les questions et la r&#233;ponse &#224; une question A n'est pas li&#233;e &#224; la r&#233;ponse &#224; une question B; par ailleurs on suppose &#233;galement que les r&#233;ponses des r&#233;pondants sont ind&#233;pendantes les uns des autres (pas de triche entre les r&#233;pondants).
    </p>
    <p>
      
    </p>
    <p>
      <b>Cons&#233;quences th&#233;oriques</b>&#160;: avec le mod&#232;le &#224; 1 seul param&#232;tre (difficult&#233;) il y a invariance du classement des r&#233;pondants par rapport aux items choisis (car la difficult&#233; est la m&#234;me pour tout le monde, donc il n'y a que la comp&#233;tence qui fait la diff&#233;rence; il y a aussi invariance de la difficult&#233; par rapport aux r&#233;pondants (la difficult&#233; &#233;tant la m&#234;me pour tous, ind&#233;pendamment de leur comp&#233;tence).
    </p>
    <p>
      
    </p>
    <p>
      <b>Popularit&#233; : </b>
    </p>
    <p>
      La TRI&#160;s'est impos&#233;e comme la meilleure m&#233;thode d'&#233;valuation, par ses avantages th&#233;oriques, mais ne s'est d&#233;mocratis&#233;e qu'avec l'av&#233;nement de l'informatique, car elle n&#233;cessite des calculs complexes it&#233;ratifs.
    </p>
    <p>
      Le champ d'application est vaste, notamment dans l'&#233;ducation.
    </p>
    <p>
      La m&#233;thode d'&#233;valuation adaptative n&#233;cessite l'utilisation de l'IRT pour &#234;tre efficace.
    </p>
