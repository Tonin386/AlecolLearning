    <p>
      <b><font size="4">Th&#233;orie classique d'&#233;valuation de comp&#233;tences (Classical Test Theory or CTT)</font></b>
    </p>
    <p>
      
    </p>
    <p>
      <b>Principe : </b>Th&#233;orie bas&#233;e sur le principe que le score observ&#233; d'un r&#233;pondant est &#233;gal au &quot;vrai&quot; score avec une erreur.
    </p>
    <p>
      
    </p>
    <p>
      <b>Objectif : </b>Mesurer la comp&#233;tence ou encore mesurer la difficult&#233; du test. Souvent utilis&#233; pour am&#233;liorer la fiabilit&#233; de tests en psychologie.
    </p>
    <p>
      
    </p>
    <p>
      <b>Hypoth&#232;se :</b>
    </p>
    <p>
      La th&#233;orie se focalise sur le score global et non sur les r&#233;ponses &#224; des items sp&#233;cifiques. Cela est valable si toutes les questions sont de difficult&#233; &#233;quivalente et avec le m&#234;me pouvoir de discrimination (notions de l'IRT). Donc l'hypoth&#232;se est que toutes les questions sont de difficult&#233;s &#233;quivalentes et repose aussi (comme l'IRT) sur une seule comp&#233;tence (unidimensionnalit&#233;).
    </p>
  </body>
